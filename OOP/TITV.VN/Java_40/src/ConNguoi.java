
public class ConNguoi {
	private String Ten;
	private int namsinh;
	public ConNguoi(String ten, int namsinh) {
		Ten = ten;
		this.namsinh = namsinh;
	}
	public String getTen() {
		return Ten;
	}
	public void setTen(String ten) {
		Ten = ten;
	}
	public int getNamsinh() {
		return namsinh;
	}
	public void setNamsinh(int namsinh) {
		this.namsinh = namsinh;
	}
	
}
